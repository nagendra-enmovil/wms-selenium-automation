package com.wms.data;

public class WarehouseData {
	
	private String warehouse_id;
	private String warehouse_name;
	private String adress;
	private String geo_codes;
	private String pincode;
	private String total_area;
	private String storage_area;
	private String date;
	
	public String getWarehouse_id() {
		return warehouse_id;
	}
	public void setWarehouse_id(String warehouse_id) {
		this.warehouse_id = warehouse_id;
	}
	public String getWarehouse_name() {
		return warehouse_name;
	}
	public void setWarehouse_name(String warehouse_name) {
		this.warehouse_name = warehouse_name;
	}
	public String getAdress() {
		return adress;
	}
	public void setAdress(String adress) {
		this.adress = adress;
	}
	public String getGeo_codes() {
		return geo_codes;
	}
	public void setGeo_codes(String geo_codes) {
		this.geo_codes = geo_codes;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	public String getTotal_area() {
		return total_area;
	}
	public void setTotal_area(String total_area) {
		this.total_area = total_area;
	}
	public String getStorage_area() {
		return storage_area;
	}
	public void setStorage_area(String storage_area) {
		this.storage_area = storage_area;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
